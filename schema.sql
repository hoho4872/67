-- 7. 비밀번호 힌트 질문 및 답변을 보관하는 user_password_hints 테이블 생성
CREATE TABLE IF NOT EXISTS public.user_password_hints (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id uuid REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
    email text UNIQUE NOT NULL,
    hint_question text NOT NULL,
    hint_answer text NOT NULL,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 7-1. RLS(Row Level Security) 설정
ALTER TABLE public.user_password_hints ENABLE ROW LEVEL SECURITY;

-- 7-2. 본인 힌트 정보 조회(SELECT) 및 추가(INSERT) 정책
CREATE POLICY "Users can view their own hints." 
ON public.user_password_hints FOR SELECT 
USING (auth.uid() = user_id);

CREATE POLICY "Users can insert their own hints." 
ON public.user_password_hints FOR INSERT 
WITH CHECK (auth.uid() = user_id);

-- 8. 이메일을 통해 힌트 질문을 반환하는 RPC 함수 정의
CREATE OR REPLACE FUNCTION get_user_hint_question(p_email text)
RETURNS text AS $$
DECLARE
  v_question text;
BEGIN
  SELECT hint_question INTO v_question 
  FROM public.user_password_hints 
  WHERE email = p_email 
  LIMIT 1;
  
  RETURN v_question;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 9. 힌트 답변을 확인하고 일치 시 비밀번호를 갱신하는 RPC 함수 정의
CREATE OR REPLACE FUNCTION verify_hint_and_reset_password(p_email text, p_answer text, p_new_password text)
RETURNS boolean AS $$
DECLARE
  v_real_answer text;
  v_user_id uuid;
BEGIN
  -- 이메일에 해당하는 실제 힌트 답변과 유저 ID 조회
  SELECT hint_answer, user_id INTO v_real_answer, v_user_id
  FROM public.user_password_hints
  WHERE email = p_email
  LIMIT 1;
  
  -- 답변 일치 여부 검사 (공백 제거 및 소문자 기준 대조)
  IF v_real_answer IS NOT NULL AND LOWER(TRIM(v_real_answer)) = LOWER(TRIM(p_answer)) THEN
    -- auth.users의 encrypted_password를 bcrypt 해시로 강제 갱신
    UPDATE auth.users 
    SET encrypted_password = crypt(p_new_password, gen_salt('bf'))
    WHERE id = v_user_id;
    
    RETURN true;
  ELSE
    RETURN false;
  END IF;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;



