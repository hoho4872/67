-- 5. 회원탈퇴 프로세스 DB 함수 정의 (RPC 함수)
-- delete_user() 함수 실행 시 탈퇴 로그 적재 후 유저 정보 및 식단 이력을 영구 삭제 처리합니다.
-- SECURITY DEFINER 권한으로 실행되어 RLS 정책을 우회하여 탈퇴 로그를 삽입하고 계정을 삭제합니다.
CREATE OR REPLACE FUNCTION delete_user()
RETURNS void AS $$
DECLARE
  v_email text;
  v_parts text[];
  v_local text;
  v_domain text;
  v_masked_local text;
BEGIN
  -- auth.users 테이블에서 탈퇴 유저의 이메일 조회
  SELECT email INTO v_email FROM auth.users WHERE id = auth.uid();
  
  -- 이메일 마스킹 처리 (예: abcde@example.com -> ab***@example.com)
  IF v_email IS NOT NULL AND POSITION('@' IN v_email) > 0 THEN
    v_parts := string_to_array(v_email, '@');
    v_local := v_parts[1];
    v_domain := v_parts[2];
    IF LENGTH(v_local) <= 2 THEN
      v_masked_local := RPAD(SUBSTRING(v_local FROM 1 FOR 1), LENGTH(v_local), '*');
    ELSE
      v_masked_local := SUBSTRING(v_local FROM 1 FOR 2) || REPEAT('*', LENGTH(v_local) - 2);
    END IF;
    v_email := v_masked_local || '@' || v_domain;
  ELSE
    v_email := 'unknown@email.com';
  END IF;
  
  -- 탈퇴 이력 테이블에 마스킹된 이메일 로그 추가
  INSERT INTO public.user_withdrawal_logs (user_id, email)
  VALUES (auth.uid(), v_email);

  -- 유저의 식사 이력 데이터 삭제
  DELETE FROM public.user_meals WHERE user_id = auth.uid();
  
  -- auth.users에서 유저 영구 제거
  DELETE FROM auth.users WHERE id = auth.uid();
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;


